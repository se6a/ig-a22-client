    <p>
        Die erheblichen Lärmemissionen der A22 belasten das gesamte Stadtgebiet sowie die
        angrenzende Gemeinde Lausen. Dadurch wird die Lebensqualität von über 20‘000 Einwohnerinnen
        und Einwohner stark in Mitleidenschaft gezogen. Nach Lärmschutzgesetzgebung ist der
        Lärmpegel zu hoch und muss reduziert werden.
    </p>

