        erstellt wurde, ist vor allem das Brückenbauwerk auf der Ergolz aus den 1970er Jahren
        inzwischen baufällig und dringend sanierungsdürftig geworden. Der Kanton musste als
        Notmassnahme mit Abschrankungen dafür sorgen, dass Lastwagen den seitlichen Bereich nicht
        überfahren. Ansonsten drohte die Gefahr, dass Brückenteile abbrechen können.
