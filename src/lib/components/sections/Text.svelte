    <h2>Die Ergolz ist zubetoniert.</h2>
    <p>
        Die A22 verläuft im Gewässerraum der Ergolz und der Frenke und zerstört so einen ökologisch
        wertvollen Lebensraum. Die Flussufer könnten auch ein attraktiver Naherholungsraum mit hoher
        Aufenthaltsqualität sein. Zudem werden im Bereich Liestal Süd grosse und wertvolle Flächen
        für das aus heutiger Sicht überdimensionierte Kreuzungsbauwerk beim Altmarkt verschwendet.
        Diese Strasse würde heute nie mehr bewilligt.
    </p>

